/**
 * Convert a title string to a URL-friendly slug.
 * Lowercases, replaces spaces with hyphens, removes special chars.
 */
export function slugify(text: string): string {
  return text
    .toLowerCase()
    .trim()
    .replace(/[^\w\s-]/g, '')
    .replace(/[\s_]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
}
